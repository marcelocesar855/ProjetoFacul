    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        
        CartaoDeCredito c1= new CartaoDeCredito();
        c1.setNomeTitular(jTNomeTitular.getText());
        c1.setNumCartao(Integer.parseInt(jTNumeroCartao.getText()));
        c1.setVencimentoCartao(jTDataVencimento.getText());
        c1.setCVV(Integer.parseInt(jTCvv.getText()));
        try {
            ViagemDAO.grava(v1);
            CartaoDeCreditoDAO.grava(c1);
            JOptionPane.showMessageDialog(null,"Viagem solicitada com suscesso\nAguarde o motorista.");
            setVisible(false);
            new jCadastrarViagem().setVisible(true);
        } catch (SQLException e) {
            
            System.out.println("Erro de SQL: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed


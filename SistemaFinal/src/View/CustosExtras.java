    private void btn_cust_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cust_salvarActionPerformed
        //Apos clicar em salvar ele atribui os valores digitados e adicionado na variavel E
        CustoE e = new CustoE();
        //Objeto DAO que sera inserido no banco de dados
        CustoEDAO dao = new CustoEDAO();

        if (modoCustoE.equals("Novo")) {
            /*Se for uma nova operação então ele vai pegar os dados que vão ser inseridos 
            e passar os dados para o objeto determinado */
            e.setData(c_cust_data.getText());
            e.setNome(c_cust_nome.getText());
            e.setQtd(Integer.parseInt(c_cust_qtd.getText()));
            e.setPreco(Double.parseDouble(c_cust_preco.getText()));
            dao.create(e);
        } else if (modoCustoE.equals("Editar")) {
            /*Caso a operação selecionada for "Editar" então a linha selecionada
            na tabela ira recarregar os valores para os campos para poderem ser editados*/
            e.setData(c_cust_data.getText());
            e.setNome(c_cust_nome.getText());
            e.setQtd(Integer.parseInt(c_cust_qtd.getText()));
            e.setPreco(Double.parseDouble(c_cust_preco.getText()));
            e.setId((int) tbl_cust_ex.getValueAt(tbl_cust_ex.getSelectedRow(), 0));
            dao.update(e);
        }

        readCustosExtras();
        //Limpa os campos de inserção 
        c_cust_data.setText("");
        c_cust_nome.setText("");
        c_cust_qtd.setText("");
        c_cust_preco.setText("");
        modoCustoE = "Navegar";
        ManipulaInterfaceCustoE();
    }//GEN-LAST:event_btn_cust_salvarActionPerformed


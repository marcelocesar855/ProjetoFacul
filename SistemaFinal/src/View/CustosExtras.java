    private void btn_cust_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cust_excluirActionPerformed
        //Tela de pergunta se realmente quer excluir a linha
        int resp = 0;
        resp = JOptionPane.showConfirmDialog(null, "Deseja Excluir ?");

        // Pega a linha selecionada e se a resposta for SIM exclui da tabela   
        if ((tbl_cust_ex.getSelectedRow() != -1) && (resp == JOptionPane.YES_OPTION)) {

            CustoE f = new CustoE();
            CustoEDAO dao = new CustoEDAO();

            f.setId((int) tbl_cust_ex.getValueAt(tbl_cust_ex.getSelectedRow(), 0));
            dao.delete(f);
            readCustosExtras();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir.");
        }

        modoCustoE = "Excluir";
        ManipulaInterfaceCustoE();
    }//GEN-LAST:event_btn_cust_excluirActionPerformed


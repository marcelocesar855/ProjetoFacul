    public void readCustosExtras() {
        DefaultTableModel modelo = (DefaultTableModel) tbl_cust_ex.getModel();
        //Para que não duplique os valores ao inserir
        modelo.setNumRows(0);
        //Criando o objeto DAO para ser passado na nossa Jtabel
        CustoEDAO fdao = new CustoEDAO();

        //Ira pegar os valores que estão na lista e passando para o objeto
        for (CustoE f : fdao.read()) {
            modelo.addRow(new Object[]{
                //Vao pegar os valores que estão no banco
                f.getId(),
                f.getData(),
                f.getNome(),
                f.getQtd(),
                f.getPreco()
            });
        }
    }

    public void ManipulaInterfaceCustoE() { //Manipulaçao da Tabela
//        Modo de operaçao dos botoes utilizados no projeto
        switch (modoCustoE) {

            case "Navegar":     //Somente o botao novo ira estar disponivel
                btn_cust_salvar.setEnabled(false);
                btn_cust_cancelar.setEnabled(false);
                c_cust_preco.setEnabled(false);
                c_cust_data.setEnabled(false);
                c_cust_qtd.setEnabled(false);
                c_cust_nome.setEnabled(false);
                btn_cust_novo.setEnabled(true);
                btn_cust_editar.setEnabled(false);
                btn_cust_excluir.setEnabled(false);
                break;

            case "Novo":    //Quando Apertado o botao Novo ira liberar os campos para o cadastro

                btn_cust_salvar.setEnabled(true);
                btn_cust_cancelar.setEnabled(true);
                c_cust_preco.setEnabled(true);
                c_cust_data.setEnabled(true);
                c_cust_qtd.setEnabled(true);
                c_cust_nome.setEnabled(true);
                btn_cust_novo.setEnabled(false);
                btn_cust_editar.setEnabled(false);
                btn_cust_excluir.setEnabled(false);
                break;

            case "Editar":
                btn_cust_salvar.setEnabled(true);
                btn_cust_cancelar.setEnabled(true);
                c_cust_preco.setEnabled(true);
                c_cust_data.setEnabled(true);
                c_cust_qtd.setEnabled(true);
                c_cust_nome.setEnabled(true);
                btn_cust_novo.setEnabled(true);
                btn_cust_editar.setEnabled(false);
                btn_cust_excluir.setEnabled(false);
                break;

            case "Excluir":
                btn_cust_salvar.setEnabled(false);
                btn_cust_cancelar.setEnabled(false);
                c_cust_preco.setEnabled(false);
                c_cust_data.setEnabled(false);
                c_cust_qtd.setEnabled(false);
                c_cust_nome.setEnabled(false);
                btn_cust_novo.setEnabled(true);
                btn_cust_editar.setEnabled(false);
                btn_cust_excluir.setEnabled(false);
                break;

            case "Selecao":
                //Os botões editar e exluir estaram disponiveis para fazer a alterção da tabela
                btn_cust_salvar.setEnabled(false);
                btn_cust_cancelar.setEnabled(false);
                c_cust_data.setEnabled(false);
                c_cust_nome.setEnabled(false);
                c_cust_qtd.setEnabled(false);
                btn_cust_novo.setEnabled(false);
                btn_cust_editar.setEnabled(true);
                btn_cust_excluir.setEnabled(true);
                break;

            default:
                System.out.println("Modo Invalido");

        }
    }

